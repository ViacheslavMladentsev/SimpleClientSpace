package com.mladentsev.simpleclientspace.enums;

/**
 * Перечисление возможных ролей пользователей системы.
 *
 * <p>Определяет доступные типы ролей для различных категорий пользователей:</p>
 * <ul>
 *   <li>{@code ROLE_ADMIN}: роль администратора, имеет полный доступ к ресурсам и управлению системой;</li>
 *   <li>{@code ROLE_USER}: роль обычного пользователя, ограниченный доступ к функционалу;</li>
 *   <li>{@code ROLE_MANAGER}: роль менеджера, промежуточная между обычным пользователем и администратором.</li>
 * </ul>
 */
public enum ERoles {

    /**
     * Роль администратора, обладающего максимальным уровнем прав.
     */
    ROLE_ADMIN,

    /**
     * Обычная роль пользователя с ограниченными правами.
     */
    ROLE_USER,

    /**
     * Промежуточная роль менеджера с расширенными возможностями относительно обычного пользователя.
     */
    ROLE_MANAGER

}
